        <h3 align="center">FAQ</h3>
        <p class="red">During an emergency, how/why are patients taken to certain Hospital?</p>
        <p class="black">Emergency crews try to accommodate a patient’s request to a specific hospital, even in cases when the request is to a facility other than the closest ER. Of course, there are exceptions. Patients will be transported to a facility (other than the patient’s request) based on several other factors:

Need for Specialty Center, like a trauma center (critical injuries), stroke center, or STEMI receiving facility (heart attack)
If patient requests a facility that is on ED diversion due to saturation (overcrowding)
If the patient is in extremis, like an uncontrolled airway or uncontrolled bleeding, they are always transported to the closest hospital emergency department </p>
        <p class="red">Who can ride with patients?</p>
        <p class="black">Typically one family member or friend can ride to the hospital with the patient.  Normally the family member or friend will ride in the passenger seat of the ambulance, up front.  This way they can be properly secured with a seat belt.  In addition, this allows the fire department paramedics and Emergency Ambulance personnel to work on the patient without the interference of the family or friend.  One exception is if we are transporting a small (often frightened) child.  Typically the mother or father will ride in the back of the ambulance to the ED. However a guest passenger is only allowed in the ambulance at the discretion of the transporting crew.</p>
        <p class="red">When do I booking for an ambulance? </p>
        <p class="black">You should booking in the event someone becomes suddenly ill or injured.  If you are not sure you should call 9-1-1, we suggest you go ahead and call.  Better safe than sorry.</p>
        <p class="red">I had an ambulance called for me, but I was not transported. Will I have a bill?</p>
        <p class="black">No, the only reason you will receive a bill is if you were transported in the ambulance.</p>
        <p class="red">What is a BLS ambulance?</p>
        <p class="black">BLS means Basic Life Support. A BLS ambulance is staffed and equipped to monitor the patient’s condition and recognize changes and provide basic life support measures such as oxygen and spinal immobilization. It is staffed by two EMTs (Emergency Medical Technicians.)</p>
        <p class="red">What is an ALS ambulance?</p>
        <p class="black">ALS means Advanced Life Support. An ALS ambulance is staffed and equipped to monitor the patient’s condition, recognize changes and provide advanced medical intervention when necessary to support life. An ALS ambulance is typically staffed by an EMT and an EMT-Paramedic.</p>
        <p class="red">Why won’t you discuss my claim with anyone but me?</p>
        <p class="black">The federal government has laws and regulations in place that are designed to protect your privacy (you will hear them referred to as the HIPAA laws.) These laws require us to be very careful about the medical information (called Protected Health Information or PHI) we share, and they consider the information about your ambulance trip (including the invoice) to be PHI. The questions we ask and the forms that must be filled out if we are to share your information with anyone besides you are required by law and are designed to protect you and the privacy of your medical records.</p>
